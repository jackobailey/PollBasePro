#' Get Previous Election Based on Date
#'
#' Given a vector of dates, this function provides a vector of the same length that corresponds to the date of the last election for each element in the vector. Applies only to the 1935 election onwards.
#'
#' @param x A vector of dates.
#' @export

get_last_election <- function(x){

  # Get date

  x <- dplyr::case_when(
    x >= "1935-11-14" & x < "1945-07-05" ~ "1935-11-14",
    x >= "1945-07-05" & x < "1950-02-23" ~ "1945-07-05",
    x >= "1950-02-23" & x < "1951-10-25" ~ "1950-02-23",
    x >= "1951-10-25" & x < "1955-05-26" ~ "1951-10-25",
    x >= "1955-05-26" & x < "1959-10-08" ~ "1955-05-26",
    x >= "1959-10-08" & x < "1964-10-15" ~ "1959-10-08",
    x >= "1964-10-15" & x < "1966-03-31" ~ "1964-10-15",
    x >= "1966-03-31" & x < "1970-06-18" ~ "1966-03-31",
    x >= "1970-06-18" & x < "1974-02-28" ~ "1970-06-18",
    x >= "1974-02-28" & x < "1974-10-10" ~ "1974-02-28",
    x >= "1974-10-10" & x < "1979-05-03" ~ "1974-10-10",
    x >= "1979-05-03" & x < "1983-06-09" ~ "1979-05-03",
    x >= "1983-06-09" & x < "1987-06-11" ~ "1983-06-09",
    x >= "1987-06-11" & x < "1992-04-09" ~ "1987-06-11",
    x >= "1992-04-09" & x < "1997-05-01" ~ "1992-04-09",
    x >= "1997-05-01" & x < "2001-06-07" ~ "1997-05-01",
    x >= "2001-06-07" & x < "2005-05-05" ~ "2001-06-07",
    x >= "2005-05-05" & x < "2010-05-06" ~ "2005-05-05",
    x >= "2010-05-06" & x < "2015-05-07" ~ "2010-05-06",
    x >= "2015-05-07" & x < "2017-06-08" ~ "2015-05-07",
    x >= "2017-06-08" & x < "2019-12-12" ~ "2017-06-08",
    x >= "2019-12-12" ~ "2019-12-12",
  )


  # Convert to date format

  x <- as.Date(x)


  # Return vector to user

  return(x)

}

