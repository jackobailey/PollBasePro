# 2. Get election dates ---------------------------------------------------

# First, we'll get the data from Wikipedia

election_dates <-
  rbind(
    htmltab(
      doc = "https://w.wiki/36xA",
      which = 2
    ) %>%
      select(date = 2),
    htmltab(
      doc = "https://w.wiki/36xA",
      which = 3
    ) %>%
      select(date = 2)
  )


# Next, we'll sort out the dates and remove all cases before universal
# suffrage was introduced in 1928

election_dates <-
  election_dates %>%
  mutate(
    date =
      date %>%
      dmy()
  ) %>%
  na.omit() %>%
  filter(date > "1928-01-01") %>%
  arrange(date) %>%
  tibble()


# Finally, we'll save the data to use later

usethis::use_data(
  election_dates,
  internal = FALSE,
  overwrite = TRUE
)



# 3. Create list of prime ministers ---------------------------------------

# Now, we'll get the list of British prime minister from Wikipedia then do
# some light editing.

prime_ministers <-
  htmltab(
    doc = "https://w.wiki/36wN",
    which = 2,
    header = 1:2
  ) %>%
  select(
    prime_minister = 1,
    pm_party = 6,
    start = 2,
    end = 3
  ) %>%
  tibble() %>%
  distinct()


# These data are terribly formatted, so we'll start by dealing with the
# dates so that we can filter out some of the crap.

prime_ministers <-
  prime_ministers %>%
  filter(
    !str_detect(start, "year|years|day|days"),
    !str_detect(start, "See also"),
    str_detect(start, "[:digit:]")
  ) %>%
  mutate(
    start = dmy(start),
    end =
      ifelse(
        end == "Incumbent",
        format(
          Sys.Date(),
          format = "%d %b%Y"
        ),
        end
      ) %>%
      dmy()
  )


# Now we can get rid of the crap in the prime_minister column and convert it to a
# labelled variable

prime_ministers <-
  prime_ministers %>%
  mutate(
    prime_minister =
      prime_minister %>%
      str_remove("\\s*\\([^\\)]+\\)") %>%
      str_remove("MP for.*") %>%
      str_remove("Sir ") %>%
      str_remove("[:digit:].*")
  ) %>%
  mutate(
    prime_minister =
      prime_minister %>%
      factor(levels = unique(prime_minister)) %>%
      as.numeric() %>%
      labelled(
        label = "Prime Minister",
        labels =
          1:length(unique(prime_minister)) %>%
          `names<-`(unique(prime_minister))
      )
  )


# And we'll also get rid of any brackets in the party column and
# convert it into a labelled variable

prime_ministers <-
  prime_ministers %>%
  mutate(
    pm_party =
      pm_party %>%
      str_remove("\\s*\\([^\\)]+\\)")
  ) %>%
  mutate(
    pm_party =
      pm_party %>%
      factor(levels = unique(pm_party)) %>%
      as.numeric() %>%
      labelled(
        label = "Prime Minister's Party",
        labels =
          1:length(unique(pm_party)) %>%
          `names<-`(unique(pm_party))
      )
  )


# Finally, we'll save the data to use later

usethis::use_data(
  prime_ministers,
  internal = FALSE,
  overwrite = TRUE
)



# 4. Create list of party leaders -----------------------------------------

# We'll again scrape Wikipedia for the data we need. Let's start with the
# Labour Party.

lab_ldr <-
  htmltab(
    doc = "https://w.wiki/36xE",
    which = 2
  ) %>%
  select(
    leader = 2,
    start = 4,
    end = 5
  ) %>%
  mutate(
    leader =
      leader %>%
      str_remove_all("\\s*\\([^\\)]+\\)") %>%
      str_remove("MP for.*") %>%
      str_remove("Sir "),
    start =
      start %>%
      str_remove_all("\\s*\\([^\\)]+\\)") %>%
      dmy(),
    end =
      end %>%
      str_remove_all("\\s*\\([^\\)]+\\)") %>%
      ifelse(. == "Incumbent", format(Sys.Date(), "%d %b %Y"), .) %>%
      dmy(),
    party = "Labour Party"
  ) %>%
  distinct()


# Next, we'll do the same for the Conservatives. We have to do this twice
# because they're such an old party. Sorry this code is gross.

con_ldr <-
  rbind(
    htmltab(
      doc = "https://w.wiki/36xL",
      which = 2
    ) %>%
      select(
        leader = 1,
        start = 3,
        end = 4
      ),
    htmltab(
      doc = "https://w.wiki/36xL",
      which = 3
    ) %>%
      select(
        leader = 1,
        start = 3,
        end = 4
      )
  ) %>%
  distinct() %>%
  mutate(
    leader =
      leader %>%
      str_remove_all("\\s*\\([^\\)]+\\)") %>%
      str_remove("MP for.*") %>%
      str_remove("Sir ") %>%
      str_remove(".*–") %>%
      trimws() %>%
      str_remove("[:digit:].*"),
    start =
      start %>%
      str_remove_all("\\s*\\([^\\)]+\\)") %>%
      dmy(),
    end =
      end %>%
      str_remove_all("\\s*\\([^\\)]+\\)") %>%
      str_remove(".* - ") %>%
      ifelse(. == "Incumbent", format(Sys.Date(), "%d %b %Y"), .) %>%
      dmy(),
    party = "Conservative Party"
  )


# Next, the liberals. Again, we have to do this twice.

lib_ldr <-
  rbind(
    htmltab(
      doc = "https://w.wiki/36xX",
      which = 1
    ) %>%
      select(
        leader = 1,
        start = 3,
        end = 4
      ) %>%
      filter(leader != "Leaders of the Liberal Party in the House of Commons") %>%
      mutate(
        leader =
          ifelse(
            str_detect(leader, "VACANT") == T,
            "Vacant",
            leader
          )
      ),
    htmltab(
      doc = "https://w.wiki/36xZ",
      which = 2
    ) %>%
      select(
        leader = 2,
        start = 4,
        end = 5
      )
  ) %>%
  distinct() %>%
  mutate(
    leader =
      leader %>%
      str_remove_all("\\s*\\([^\\)]+\\)") %>%
      str_remove("MP for.*") %>%
      str_remove("Sir ") %>%
      str_remove(".*–") %>%
      trimws() %>%
      str_remove(",.*") %>%
      str_remove("[:digit:].*") %>%
      str_remove("was|were.*") %>%
      str_remove("Acting.*") %>%
      trimws(),
    start =
      start %>%
      str_remove_all("\\s*\\([^\\)]+\\)") %>%
      dmy(),
    end =
      end %>%
      str_remove_all("\\s*\\([^\\)]+\\)") %>%
      str_remove(".* - ") %>%
      ifelse(. == "Incumbent", format(Sys.Date(), "%d %b %Y"), .) %>%
      dmy(),
    party = "Liberals (Various Forms)"
  ) %>%
  mutate(
    end =
      case_when(
        is.na(end) == T & is.na(lead(start, 1)) == F ~ lead(start, 1),
        is.na(end) == T & is.na(lead(start, 1)) == T ~ lead(end, 1),
        start == end ~ lead(start, 1),
        TRUE ~ end
      ),
    start =
      case_when(
        is.na(start) == T ~ lag(end, 1),
        TRUE ~ start
      )
  ) %>%
  distinct()

# Now we can combine them

party_leaders <-
  rbind(
    con_ldr,
    lab_ldr,
    lib_ldr
  )


# Finally, we'll save the data to use later

usethis::use_data(
  party_leaders,
  internal = FALSE,
  overwrite = TRUE
)



